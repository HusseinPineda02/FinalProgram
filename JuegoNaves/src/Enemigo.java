public class Enemigo {
    
   
    private String tipo;
    private int salud;
    private int ataque;
    

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public void setSalud(int salud) {
        this.salud = salud;
    }

    public void setAtaque(int ataque) {
        this.ataque = ataque;
    }

    public String getTipo() {
        return tipo;
    }

    public int getSalud() {
        return salud;
    }

    public int getAtaque() {
        return ataque;
    }
    
    
}
